package demo.demo1;

public class MathUtils {

    public void adunare(int x, int y){
        System.out.println(x+y);
    }
    public void adunare(double x, double y){
        System.out.println(x+y);
    }
    public void scadere(int x, int y){
        System.out.println(x-y);
    }
    public void scadere(double x, double y){
        System.out.println(x-y);
    }
    public void inmultire(int x, int y){
        System.out.println(x*y);
    }
    public void inmultire(double x, double y){
        System.out.println(x*y);
    }

}
